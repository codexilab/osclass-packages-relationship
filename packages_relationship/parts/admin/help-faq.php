<!-- FAQ - Why I have not inherited the package of the company? -->
<h2 class="render-title">I am User type account and I have joined a company, why I have not inherited the package of the company?</h2>
<div class="form-row">
	<p>When a user account is a member of a company, it does not automatically inherit the package from the company.</p>
	<p>The owner of the company's account must click on 'Inherit' to the members who wish they can use their publications package.</p>
</div>

<div style="clear: both;"><br><br></div>

<!-- FAQ - Why I have not inherited the package of the company? -->
<h2 class="render-title">I am member of a company with its package inherited, but I don't want to use it and at the same time I do not want to leave the company, what do I do?</h2>
<div class="form-row">
	<p><span>1)</span> Go to Update account (from user logged).</p>
	<p><span>2)</span> Unmark 'Use package' field.</p>
	<p><span>3)</span> Update.</p>
</div>

